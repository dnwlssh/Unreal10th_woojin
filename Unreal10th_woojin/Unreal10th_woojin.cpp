#include <iostream>

int main()
{
    printf("Hello World!\n헬로 월드\n");    // \n 줄바꿈
    printf("Hello World!\t헬로 월드\n");    // \t 탭
    printf("Hello\t\t헬로 월드\n");
    printf("\'Hello World!\'\n");   // 작은 따옴표
    printf("\"Hello World!\"\n");   // 큰 따옴표
    printf("\\Hello World!\\\n");   // \(백 슬래시)
}